package pl.edu.pjwstk.cms.models;

import java.util.logging.Logger;
import pl.edu.pjwstk.cms.models.general.DatabaseObject;

/**
 *
 * @author Macha
 */
public class SystemConfiguration extends DatabaseObject{
    
    private final static Logger LOGGER = Logger.getLogger(SystemConfiguration.class.getName());
    
    private String name;
    private String description;
    private String value;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }

    
    public SystemConfiguration(){
        super();
    }
}
